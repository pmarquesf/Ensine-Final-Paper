                <div class="texto-conteudo">
                      A Revolução Francesa foi um dos acontecimentos mais importantes da história contemporânea do Ocidente. Ela marcou o fim da Idade Moderna e o início da Idade Contemporânea, de acordo com o que foi estipulado por historiadores europeus. O motivo para isso foram as influências da Revolução Francesa no mundo.<br><br>

                      A Revolução Francesa marcou a ascensão da burguesia como classe social dominante, superando a aristocracia proprietária de terras, bem como a criação de novas instituições e novas formas de organizar a vida econômica, política e social que iriam se expandir para todo o planeta.<br><br>

                      Com a Revolução Francesa, o capitalismo rompeu os obstáculos políticos feudais que ainda vigoravam na Europa Ocidental, juntando-se às transformações econômicas desencadeadas com a Revolução Industrial.<br><br>

                      Essas mudanças vinham sendo preparadas desde os séculos XVII e XVIII, com o desenvolvimento do pensamento racional Iluminista. Para os iluministas, a razão poderia auxiliar todos os homens na explicação dos fenômenos da natureza e da forma de organização da sociedade.<br><br>

                      Não que os iluministas fossem essencialmente revolucionários. Mas as ideias iluministas serviram, junto à utilização da razão para interpretar o mundo, para os revolucionários franceses questionaram o caráter sagrado do poder, defendido pelos reis, pela aristocracia e pela Igreja.<br><br>

                      Todos os homens poderiam exercer o poder. Mas para isso era necessário criar instituições que garantissem esse exercício. Nesse sentido, a República foi a principal dessas instituições. Ela representava o fim dos privilégios da aristocracia e a libertação dos camponeses dos laços de servidão que os prendiam à nobreza e ao clero. Nas cidades, tinham fim as corporações feudais que limitavam os negócios da burguesia.<br><br>

                      Mas mesmo antes da Revolução Francesa, os ideais iluministas já haviam possibilitado que na América do Norte os colonos ingleses realizassem a Independência dos EUA e construíssem também uma República. Mas o impulso maior foi mesmo dado pela Revolução Francesa, graças ao poderio do Estado francês.<br><br>

                      A Revolução Francesa influenciou ainda outros processos de independência no continente americano. Em 1794, os africanos escravizados que trabalhavam nas lavouras de cana-de-açúcar do Haiti conseguiram o fim da escravidão após uma sanguinária guerra de independência. Era o primeiro país do continente a por fim à escravidão. No Brasil, a Conjuração Baiana (ou Revolta dos Alfaiates) de 1798 também foi fortemente influenciada pelos acontecimentos da Revolução Francesa.<br><br>

                      O historiador Eric. J. Hobsbawm afirma ainda que os revolucionários franceses influenciaram simbolicamente as nações emergentes da Europa do século XIX com as bandeiras tricolores.<br><br>

                      O conceito e o vocabulário do nacionalismo, desenvolvido com educação universal, e o recrutamento de cidadãos de todas as classes para o exército foram mais umas das influências da Revolução Francesa.<br><br>

                      O modelo de organização técnica e científica, além do sistema métrico de medidas (metro, centímetro, decímetro etc.), foi outra contribuição desenvolvida nesse período.<br><br>
