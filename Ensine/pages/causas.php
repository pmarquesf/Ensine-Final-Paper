    <div class="titulo"> As Origens da Revolução</div>
          <div class="box-conteudo">
            <div class="subtitulo" style="font-size: 25px"> Causas da Revolução</div>
                <div class="texto-conteudo">
                      A Revolução Francesa não aconteceu sem aviso na história da França, tampouco a forma de pensar que  norteou os ideais dos revolucionários surgiu do dia para a noite. Entretanto, nem Luis XVI nem a Igreja estavam preparados para o que se iniciou em 1789 e se estendeu até o último ano do século XVIII. Podemos mencionar 4 como sendo as causas principais da revolução:<br><br>
                      - O pensamento iluminista;<br>
                      - A influência da Revolução Americana na economia e no imaginário da França;<br>
                      - A desigualdade entre diferentes grupos sociais;<br>
                      - Uma grande crise econômica que gerou fome e mortes.<br>
                      - Todas essas razões estão ligadas entre si, mas vamos entender o que foi cada uma.
                </div>
            </div>

          <div class="box-conteudo">
            <div class="subtitulo"> O Iluminismo e o Século das Luzes</div>
                <div class="texto-conteudo">
                      O Século das Luzes, como ficou conhecido o século XVIII, foi um período com grandes mudanças na forma como as pessoas por toda a Europa lidavam com o conhecimento, e foi o momento do despertar de uma das correntes de pensamento mais importantes da história: O Iluminismo.<br>

                      O Iluminismo surgiu no começo do século, e teve grande influência sobre os intelectuais e a burguesia daquele período em toda a Europa. Esse movimento defendia valores do humanismo e da razão, colocando o ser humano e a busca pelo conhecimento científico como as fontes das quais deveriam emanar todo o poder e as decisões da sociedade. No pensamento iluminista, não havia mais espaço para monarcas com poderes absolutos nem para a crença religiosa como a fonte para justificar estruturas de poder.<br>

                      Os ideais iluministas eram disseminados facilmente por todo o país, com textos circulando por gazetas e folhetins. Essa corrente de pensamento prosperou entre os intelectuais da Europa desde a França, com pensadores como Voltaire, Jean-Jacques Rousseau, e Mostesquieu, até a Itália, Alemanha e Inglaterra, onde viveu John Locke, o “pai do Liberalismo”. Esses nomes são conhecidos até hoje como baluartes da liberdade e dos direitos individuais dos cidadãos. 
                </div>
            </div>

          <div class="box-conteudo">
            <div class="subtitulo"> A influência da Revolução Americana</div>
                <div class="texto-conteudo">
                      A segunda metade do século XVIII não foi marcada apenas pela Revolução Francesa, mas por uma série de revoltas e revoluções ao redor do continente europeu e na América. Antes de a revolução na França começar em 1789, a mais notável havia sido a Revolução de Independência das colônias americanas.<br>

                      Essa revolução foi apoiada por Luis XVI, que enviou tropas francesas para ajudar George Washington e companhia na empreitada americana de se tornar independente da Grã-Bretanha, o principal rival político e econômico da França naquele período. A empreitada francesa, apesar de bem vista pela população, trouxe um prejuízo financeiro e dívida tão grandes para a coroa que obrigou Luís XVI a fazer sucessivos aumentos de impostos ao longo das décadas de 1770 e 1780, período em que a França já passava por uma grande crise econômica, que veremos em breve.<br>

                      A Revolução Americana foi importante também por um motivo simbólico: fortalecer no imaginário dos franceses os ideais de liberdade e de igualdade entre cidadãos, em sintonia com o pensamento iluminista da época. Porém, esses desejos cada vez mais claros do povo francês se chocavam com uma barreira poderosa: a divisão brutal da sociedade em grupos, alguns com muitos privilégios, e outro com nenhum. Esses grupos eram chamados de Estados. 
                </div>
            </div>

          <div class="box-conteudo">
            <div class="subtitulo"> Os Três Estados na França</div>
                <div class="texto-conteudo">
                      A França era uma superpotência europeia no fim do século XVIII, com uma população de aproximadamente 25 milhões de habitantes e uma das monarquias mais vitoriosas do continente. Entretanto, existia uma desigualdade latente entre nobres, membros da Igreja, burgueses e camponeses, e essa desigualdade existia por meio da existência de três ordens hierárquicas distintas, conhecidas como Primeiro, Segunda e Terceiro Estados.<br><br>

                      O Primeiro Estado<br>
                      O Primeiro Estado francês era composto pelo clero da Igreja Católica, que ao longo da Idade Média exerceu uma hegemonia religiosa e política na França com poderes comparáveis aos do rei. Uma figura de destaque que representa bem o quão forte eram os membros da Igreja é o Cardeal de Richelieu, que no início do século XVII, como ministro do rei Luis XIII, liderou os exércitos da França em guerras por todo do continente, como a Guerra dos Trinta Anos.<br>

                      Mas o clero não contava apenas com o poder de influenciar o rei em suas decisões, a Igreja também era proprietário de mais de 20% das terras da França e detinha uma fortuna significativa em ouro e outros bens.<br><br>

                      O Segundo Estado <br>
                      A nobreza compunha o Segundo Estado francês, e era formada pelos aristocratas. Esse grupo estava em decadência antes de a revolução começar, pois vinham de uma tradição feudal de vassalagem que estava se dissolvendo conforme os camponeses se tornavam livres e deixavam as terras dos senhores. Essa aristocracia, que baseava seu poder em títulos e influência por meio de cargos públicos, representava algo que a revolução queria derrubar: os privilégios.<br><br>

                      O Terceiro Estado<br>

                      “O que é o Terceiro Estado? – Tudo.<br>

                      O que ele tem sido até agora na política da França? – Nada.<br>

                      O que ele quer? – Quer ser algo.”<br><br>

                      É com essas três frases que o escritor francês Emmanuel Joseph Sievès (03/05/1748 – 20/06/1836) iniciou seu tratado pré-revolucionário sobre o que era o Terceiro Estado. Mas você já consegue imaginar quais cidadãos faziam parte desse grupo?<br>

                      O Terceiro Estado era composto pela burguesia que estava nascendo na França naquele período, pelos trabalhadores das cidades e pelos camponeses, sendo que esses últimos correspondiam a quase 80% do total da população francesa.
                </div>
            </div>

            <div class="box-conteudo">
            <div class="subtitulo"> A crise econômica da França</div>
                <div class="texto-conteudo">
                      ‘Se não têm pão, que comam brioches”. Essa frase é atribuída à rainha Maria Antonieta, e embora provavelmente nunca tenha sido dita de fato, representa bem a situação em que a população francesa se encontrava em 1789 e a visão que a nobreza tinha sobre ela.<br>

                      Durante as décadas de 1770 e 1780, a França passou por uma grande estiagem, o que gerou uma diminuição significativa das colheitas de trigo e outros cereais, que eram a principal fonte de alimento dos camponeses. Essa crise, em conjunto com os sucessivos aumentos de impostos sofridos apenas pelos mais pobres, causou um estado de miséria entre camponeses e trabalhadores das cidades no país. Houve muitas mortes por inanição e saqueamentos, além de conflitos nas ruas por comida.<br>

                      Essa situação obrigou o rei Luís XVI a tomar uma atitude para tentar resolver a crise dos Franceses. A decisão do rei foi convocar a Assembléia dos Estados Gerais. Essa Assembléia é considerada o ponto sem volta para as mudanças que estavam por vir, e se ela não tivesse sido convocada, talvez a Revolução não ocorresse. Ficou curioso para saber o que foi esse grande evento?
