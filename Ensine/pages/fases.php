     <div class="box-conteudo"><a name="diretorio"></a>
            <div class="subtitulo" style="font-size: 25px"> Diretório (1795-1799)</div>
                <div class="texto-conteudo">
                     O Diretório, criado em 1795, marcou a retomada de poder dos girondinos na condução do processo revolucionário francês. Aproveitando toda a instabilidade política deixada pelos radicais que capitanearam a Convenção Nacional, a alta burguesia conseguiu reassumir o país promovendo reformas que deram fim às medidas populares criadas anteriormente. Daí em diante, a consolidação de qualquer mudança mais profunda seria controlada por instrumentos de visível exclusão política.<br><br>

                    Entre as primeiras medidas tomadas, o novo governo criou uma nova constituição que colocava cinco integrantes à frente do Poder Executivo. Estes membros do chamado Diretório seriam escolhidos por meio da votação do Poder Legislativo que, por sua vez, voltariam a ser escolhidos pelo sistema de censitário. Por meio dessas medidas, buscava-se evitar outra possível radicalização política e, ao mesmo tempo, estabilidade necessária para se lutar contra os problemas internos e as tropas inimigas.<br><br>

                    A composição do cenário político ganhou uma nova configuração, onde os girondinos ocupavam a parte central da assembléia legislativa, os realistas ficavam à direita e os poucos jacobinos que restavam situados à esquerda. Nessa transição houve uma tentativa de restauração da monarquia através de um golpe. Contudo, um jovem militar chamado Napoleão Bonaparte foi responsável por conter a trama que colocou a revolução sob ameaça.<br><br>

                    Da mesma forma, os radicais também tentaram abalar o poder burguês restaurado por meio de uma grande revolta popular. Graco Babeuf, líder principal da chamada “conspiração dos iguais”, realizava duras críticas contra a manutenção da propriedade privada e defendia a volta de um governo popular capaz de extinguir qualquer tipo de desigualdade. Apesar de tais incidentes, o Diretório continuava a realizar as mudanças favoráveis ao projeto político burguês.<br><br>

                    Contrastando com as disputas que agitavam o cenário político interno, a França conseguia vitórias cada vez mais significativas contra os exércitos absolutistas europeus. Nas batalhas ocorridas na Europa Oriental, em Malta, na Suíça, no Egito e na Síria os franceses impunham novas derrotas contra aqueles que temiam o avanço do ideal revolucionário. Entre tantos combatentes, a figura do general Napoleão Bonaparte se tornava unânime entre os defensores da soberania nacional francesa.<br><br>

                    Em pouco tempo, as vitórias de Napoleão se transformaram em uma segura via de sua própria ascensão política. Convidado para participar do governo francês, Napoleão aproveitou o momento favorável para articular um golpe de Estado contra o Diretório. Com o apoio de membros expressivos da alta burguesia, o comandante militar realizou o golpe de 18 de Brumário, correspondente ao dia 9 de novembro de 1799, segundo o calendário cristão.<br><br>

                    O Diretório passou a dar lugar ao Consulado, instituição integrada por três representantes entre os quais estavam Roger Ducos, o abade de Seyès e o próprio Napoleão Bonaparte. Na prática, o general corso concentrava as mais importantes decisões em suas mãos e, dessa forma, trilhava o caminho que posteriormente o elevaria à posição de imperador da França. Apesar da aparência monárquica, o vindouro fato consagraria as aspirações burguesas que movimentaram a Revolução Francesa.<br><br>
