            <div class="subtitulo" style="font-size: 25px"> Monarquia Constitucional (1789-1792)</div>
                <div class="texto-conteudo">
                      Com a dissolução da Assembléia Nacional, o governo francês agora colocava o rei sob a tutela de uma lei válida para todos os cidadãos. Apesar do expressivo significado dessa conquista, essa nova carta constitucional tinha visíveis limites que tentavam afastar os populares das questões políticas nacionais. Entre outros pontos, a recém-promulgada constituição francesa estabelecia a comprovação de uma renda mínima para que os cidadãos pudessem votar.<br><br>

                      Nesse mesmo período, o rei Luís XVI sentia o enfraquecimento de seu poder de intervenção política dentro do país. Com isso, tentou arquitetar uma contra-revolução por meio de um mal fadado plano de fuga que o colocaria em contato com outros nobres franceses exilados em outras monarquias absolutistas da Europa. Ao tentar sair do país disfarçado, acabou sendo reconhecido na cidade de Varennes. Depois disso, foi reconduzido para a capital e mantido sob forte vigilância.<br><br>

                      Enquanto o rei era transformado em um refém dos revolucionários franceses, o cenário político interno tomava feições bastante problemáticas. Dentro do Poder Executivo, representado pela Assembléia Nacional, dois grupos políticos discutiam os novos rumos a serem tomados pela nação. De um lado estavam os girondinos, grupo político formado por membros da alta burguesia e do outro os jacobinos, representantes de pequenos burgueses e das classes populares.<br><br>

                      Estes últimos, ao contrário dos jacobinos, defendiam a urgência de reformas mais profundas que pudessem atender prontamente as reivindicações dos menos favorecidos. Enquanto isso, as medidas econômicas adotadas pelo novo governo não alcançavam os efeitos esperados. A emissão excessiva de assignats dentro da economia francesa provocou uma nova onda inflacionária que colocava em risco a estabilidade política necessária ao desenvolvimento da revolução.<br><br>

                      Externamente, as demais nações européias já assistiam com apreensão os rumos tomados pela agitação política que tomava conta da França. Alguns anos após o estouro da revolução na França, as camadas populares de outras nações absolutistas como Suíça, Holanda e Bélgica começavam a simpatizar com os princípios e mudanças defendidos pela experiência francesa. Dessa forma, os monarcas de diferentes potências européias se mobilizaram em um potente movimento contra-revolucionário.<br><br>

                      Logo depois da Declaração de Pillnitz, onde vários monarcas anunciavam a possibilidade de promover uma invasão à França, as tropas prussianas e austríacas declaram guerra contra a Revolução. Acuadas, as lideranças francesas anunciaram a “pátria em perigo”. Com isso, decidiu-se distribuir armas a toda população francesa e a apresentação obrigatória de todos os homens aptos para a luta. A partir de então, uma comuna liderada por Marat, Danton e Robespierre tomou conta das ruas de Paris.<br><br>

                      No dia 20 de setembro de 1792, o exército popular francês conseguiu vencer a coligação militar austro-prussiana na Batalha de Valmy. Após a vitória, esse mesmo exército popular promoveu a detenção de Luís XVI, considerado traidor do ideal revolucionário. Além disso, exigiu a queda dos poderes exercidos pela Assembléia Nacional e o conseqüente fim da Monarquia Constitucional. Logo em seguida, foi realizada a proclamação de um regime republicano para a França. <br><br>
