    <div class="box-conteudo"><a name="convencao"></a> 
            <div class="subtitulo" style="font-size: 25px"> Convenção Nacional (1792-1795)</div>
                <div class="texto-conteudo">
                      A fase da Convenção Nacional da Revolução Francesa marca a tomada de poder a ser realizada pelas camadas populares francesas. Nesse período a tomada das armas para a participação na Batalha de Valmy e a insatisfação dada pelos resultados da monarquia constitucional motivou a convocação de uma Convenção Nacional. Essa nova instituição haveria de elaborar uma nova carta constituinte que viesse a superar os limites do regime anterior.<br><br>

                      Refletindo a agitação da época a Convenção foi formada por uma maioria de jacobinos, setor que representava a pequena burguesia e os sans-culottes. Além disso, haviam os girondinos, partido contrário à radicalização do processo revolucionário e defensor das conquistas que prestigiavam os interesses políticos e econômicos da parcela mais abastada da burguesia. Foi a partir desses dois grupos, um mais exaltado e outro conservador, dos quais herdamos os conceitos políticos de “direita” e “esquerda”.<br><br>

                      Demonstrando a natureza insurgente dessa nova fase, a Convenção decidiu criar um novo calendário que fixava o dia 22 de setembro de 1792 como o primeiro dia do ano I da República. Abandonando a contagem de tempo instituída pelo calendário cristão, a nova metragem do tempo seria medida por meio de diferentes ciclos agrícolas e naturais. Na verdade, tal medida simbolizava somente a primeira e menor das reviravoltas a serem determinadas pela Convenção.<br><br>

                      Em suas primeiras atividades, o novo regime republicano descobriu uma série de documentos que comprovavam as negociações do rei Luis XVI junto à monarquia austríaca para que fosse possível combater o processo revolucionário francês. Em pouco tempo, um processo judicial acusou o rei da França de traição e, mediante grande pressão popular, o os membros da Convenção decidiram condená-lo à guilhotina no dia 21de janeiro de 1793.<br><br>

                      A divulgação da notícia pela Europa motivou as monarquias do continente a formarem novas forças que combatessem o quadro político francês. Inglaterra, Áustria, Holanda, Prússia, Espanha, Sardenha e Rússia se mobiliaram em torno da Primeira Coligação. Internamente, os problemas econômicos e as contendas políticas permitiram a formação de uma revolta anti-republicana, estimuladas pelos nobres, por vários habitantes da região da Vendéia.<br><br>

                      Para contornar a difícil situação, os membros da Convenção realizaram o decreto de “pátria em perigo” e, logo em seguida, criaram um órgão encarregado da defesa e da ordem interna conhecido como Comitê de Salvação Pública. Para atender os populares permitiu a criação do sufrágio universal, anulou os direitos feudais remanescentes, permitiu a formação de pequenas propriedades e tabelou o preço de todos os gêneros de primeira necessidade.<br><br>

                      Entre outras medidas, esse novo governo também defendeu a criação da escola primeira pública e gratuita, a regulação dos salários, o direito de greve e o combate ao estado de miséria que atingisse qualquer cidadão. Apesar de buscar soluções, as medidas tomadas por esse novo regime não conseguiam suportar a crise que atingia a República por quase todos os lados. Dessa forma, entre 1793 e 1794, a perseguição sistemática dos traidores do ideal revolucionário marcou a fase do Terror dentro da revolução.<br><br>

                      Enquanto o Comitê de Salvação Nacional buscava resolver os problemas internos, o Tribunal Revolucionário perseguia e condenava à morte qualquer um que viesse a ser visto como desleal à revolução. A ação desenfreada desse órgão instigou vários setores políticos a se voltarem contra um governo capaz de guilhotinar todo aquele que não concordasse com o governo. Não por acaso, mais de 15 mil pessoas, entre girondinos e jacobinos, foram condenadas à morte.<br><br>

                      O caos instalado pela atuação política de figuras como Maximilien Robespierre e Saint-Just acabou enfraquecendo o apoio anteriormente concedido pelos sans-culottes. A desordem, a ameaça inimiga e a miséria logo se voltariam contra aqueles que promoviam o derramamento de sangue em nome da revolução. Foi nesse contexto que os oponentes às alas radicais se uniram e conseguiram, em 24 de julho de 1794, derrubar os líderes da convenção.<br><br>

                      Tal fato viria a ficar reconhecido como a “reação termidoriana”, que teve o nome inspirado no mês corrente em que aconteceu tal reviravolta política. Com a condenação e morte de Robespierre e Saint-Just os membros da alta burguesia acharam meios para reassumir os destinos da Revolução Francesa. Em pouco tempo, as medidas jacobinas foram anuladas e uma nova constituição – a Constituição do Ano III – deu origem a um novo órgão executivo: o Diretório.<br><br>
